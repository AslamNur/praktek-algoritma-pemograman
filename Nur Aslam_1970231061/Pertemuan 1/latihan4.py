#tipe data boolean
print(True)

#tipe data string
print("ayo belajar pyton")
print('belajar pyton sangat mudah')

#tipe data integer
print(20)

#tipe data float
print(3.14)


#tipe data complex
print(5j)

#tipe data list
print(1,2,3,4,5)
print(["satu", "dua", "tiga"])

#tipe data tuple
print((1,2,3,4,5))
print(("satu", "dua", "tiga"))

#tipe data dictionary
print({"nama":"budi", 'umur': 20})
#tipe data dictionary dimasukan kedalam variable biodata
biodata = {"nama":"andi", 'umur':21}
#proses inisialisasi variable biodata
print(biodata)
#proses pencetakan variable biodata yang berisi tipe data dictionary
print(type(biodata))
#funsi untuk mengecek jenis tipe data. akan tampil <class 'dict'> yang berarti dict adalah type data dictionary